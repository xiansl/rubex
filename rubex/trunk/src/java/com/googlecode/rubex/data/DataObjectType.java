package com.googlecode.rubex.data;

/**
 * Type of data object.
 * 
 * @author Mikhail Vladimirov
 */
public enum DataObjectType
{
    /**
     * Null data object.
     */
    NULL,
    
    /**
     * Boolean data object.
     */
    BOOLEAN,
    
    /**
     * Integer data object.
     */
    INTEGER,
    
    /**
     * Real data object.
     */
    REAL,
    
    /**
     * String data object.
     */
    STRING,
    
    /**
     * Binary data object.
     */
    BINARY,
    
    /**
     * List data object.
     */
    LIST,
    
    /**
     * Structure data object.
     */
    STRUCTURE
}
